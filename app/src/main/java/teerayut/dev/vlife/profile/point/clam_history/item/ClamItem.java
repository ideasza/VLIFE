public class ClamItem {

    private String clamDate;
    private String clamDetail;
    private String clamStatus;
    private String clamPoints;

    public ClamItem() {

    }

    public String getClamDate() {
        return clamDate;
    }

    public void setClamDate(String clamDate) {
        this.clamDate = clamDate;
    }

    public String getClamDetail() {
        return clamDetail;
    }

    public void setClamDetail(String clamDetail) {
        this.clamDetail = clamDetail;
    }

    public String getClamStatus() {
        return clamStatus;
    }

    public void setClamStatus(String clamStatus) {
        this.clamStatus = clamStatus;
    }

    public String getClamPoints() {
        return clamPoints;
    }

    public void setClamPoints(String clamPoints) {
        this.clamPoints = clamPoints;
    }
}
